# Exemplo de variáveis em Python

# Números inteiros
numero_inteiro = 42
print("Número inteiro:", numero_inteiro)

# Números flutuantes ("Numero com virgula")
numero_flutuante = 3.14
print("Número flutuante:", numero_flutuante)

# Strings
texto = "Olá, mundo!"
print("String:", texto)

# Listas
lista_de_numeros = [1, 2, 3, 4, 5]
print("Lista de números:", lista_de_numeros)

# Tuplas
tupla_de_cores = ("vermelho", "verde", "azul")
print("Tupla de cores:", tupla_de_cores)

# Conjuntos
conjunto_de_animais = {"cachorro", "gato", "pássaro"}
print("Conjunto de animais:", conjunto_de_animais)

# Dicionários
dicionario_de_telefones = {"Maria": "1234-5678", "João": "8765-4321"}
print("Dicionário de telefones:", dicionario_de_telefones)
